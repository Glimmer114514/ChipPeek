def main():
    i18n_dir = os.path.join(os.path.dirname(os.path.abspath(__file__)), "i18n")
    os.makedirs(i18n_dir, exist_ok=True)

    # 写入英文基础文件
    from i18n import TRANSLATIONS as base_trans
    for lang_code, data in base_trans.items():
        file_path = os.path.join(i18n_dir, f"{lang_code}.json")
        with open(file_path, 'w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
        print(f"写入 {lang_code}.json")

    # 写入其他语言翻译
    for lang_code, data in TRANSLATIONS.items():
        file_path = os.path.join(i18n_dir, f"{lang_code}.json")
        with open(file_path, 'w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
        print(f"写入 {lang_code}.json")

    print(f"\n完成！共生成 {len(base_trans) + len(TRANSLATIONS)} 个语言文件")
    print(f"目录: {i18n_dir}")


if __name__ == "__main__":
    main()
