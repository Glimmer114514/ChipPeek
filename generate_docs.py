def generate_nav_links(current_lang):
    """生成语言切换导航链接"""
    links = []
    for code, name in LANGUAGE_NAMES.items():
        if code == current_lang:
            links.append(f"**{name}**")
        else:
            links.append(f"[{name}](README_{code}.md)")
    return " | ".join(links)


def render_readme(lang, data):
    """渲染单语言README"""
    lines = []

    # 标题和副标题
    lines.append(f"# {data['title']}")
    lines.append("")
    lines.append(data["subtitle"])
    lines.append("")

    # 语言导航
    lines.append(f"> **{data['nav_label']}**: {generate_nav_links(lang)}")
    lines.append("")

    # 功能特性
    lines.append(f"## {data['features_title']}")
    lines.append("")
    for feat in data["features"]:
        lines.append(f"- {feat}")
    lines.append("")

    # 快速开始
    lines.append(f"## {data['quick_start_title']}")
    lines.append("")
    lines.append(f"### {data['direct_use_title']}")
