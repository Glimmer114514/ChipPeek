def main():
    docs_dir = os.path.join(os.path.dirname(os.path.abspath(__file__)), "docs")
    os.makedirs(docs_dir, exist_ok=True)

    for lang, data in READMES.items():
        content = render_readme(lang, data)
        file_path = os.path.join(docs_dir, f"README_{lang}.md")
        with open(file_path, 'w', encoding='utf-8') as f:
            f.write(content)
        print(f"生成 README_{lang}.md")

    # 将英文版作为主 README（放在根目录）
    en_content = render_readme("en", READMES["en"])
    root_readme = os.path.join(os.path.dirname(os.path.abspath(__file__)), "README.md")
    with open(root_readme, 'w', encoding='utf-8') as f:
        f.write(en_content)
    print(f"\n更新根目录 README.md (English)")

    print(f"\n完成！共生成 {len(READMES)} 个语言版本")
    print(f"目录: {docs_dir}")


if __name__ == "__main__":
    main()
